# 數字型態皆可以使用數學運算子來計算
# + 加法
# - 減法
# * 乘法
# / 浮點除法
# // 整數除法
# % 取餘數
# ** 乘冪

# 1. 整數
# 所有連續的數字都會被視為常值整數
print(5)
print(0)
print(+5)
print(-5)
# print(05) # 以0開頭的數字會跳錯誤

print(5 + 9)
print(100 - 7)
print(4 - 10)
print(5 + 9 + 3)
print(4 + 3 - 2 - 1 + 6)
print(5 + 9                   + 3)
print(6 * 7)
print(8 / 3) # 算出浮點
print(8 // 3) # 算出整數
print(7 / 0) # 任何數除以0都會跳錯誤
print(5 // 0) # 任何數除以0都會跳錯誤
print(8 % 2)
print(2**3)

